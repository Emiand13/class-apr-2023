const [a, b] = [1, 10];

console.log(a * b); // En este caso el console log lo que nos pide es que multipliquemos a * b y el resultado es 10

// Se utiliza la destructuración de arrays para asignar los valores del array [1, 10] a las variables a y b.

// La sintaxis [a, b] indica que se desea asignar el primer elemento del array a la variable a y el segundo elemento a la variable b.

// En este caso, el array [1, 10] tiene dos elementos, 1 y 10. Entonces, 1 se asigna a la variable a y 10 se asigna a la variable b.

// Luego, se realiza la operación a * b, que es 1 * 10, resultando en 10.

// Finalmente, el resultado 10 se imprime en la consola utilizando console.log(a * b).





